

// Partials
// Partials wrap an object, make the object's properties optional and allow
// you to build that object property by property rather than at once.
// Think of this as allowing you to use the builder patterns for a defined
// object type.
// At the end of your operation, you return the working object by casting it as
// the original object.
// So you get what you wanted + additional type-safety during the building
// process.
// 
// Partial<OriginalType>




